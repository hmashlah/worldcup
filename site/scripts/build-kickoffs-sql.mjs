#!/usr/bin/env node
// Generate the SQL needed to seed wc26_match_kickoffs from public/data.json.
//
// Why this exists: the RLS policies on wc26_predictions consult
// wc26_match_kickoffs to decide whether a write is still allowed. The schedule
// itself lives in tournament data (public/data.json), so we mechanically emit
// one INSERT row per match here. Re-run this whenever the schedule changes
// (kickoff slipped, KO slot resolved, etc.) and paste the output into the
// Supabase SQL editor.
//
// Usage (from repo root or anywhere):
//   node site/scripts/build-kickoffs-sql.mjs > kickoffs.sql
//   # then paste kickoffs.sql contents into Supabase SQL editor

import { readFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const dataPath = resolve(here, '..', 'public', 'data.json');
const raw = await readFile(dataPath, 'utf8');
const data = JSON.parse(raw);

/**
 * Convert "2026-06-11" + "13:00 UTC-6" → ISO 8601 with offset.
 * The offset is local to the host stadium; we hand it to Postgres verbatim
 * so TIMESTAMPTZ stores the absolute instant correctly.
 */
function toIsoTimestamp(date, time) {
  const m = /^(\d{2}):(\d{2})\s+UTC([+-]\d+)$/.exec(time.trim());
  if (!m) throw new Error(`Cannot parse time: ${JSON.stringify(time)}`);
  const [, hh, mm, off] = m;
  const sign = off.startsWith('-') ? '-' : '+';
  const hours = off.replace(/^[+-]/, '').padStart(2, '0');
  return `${date}T${hh}:${mm}:00${sign}${hours}:00`;
}

const rows = [];
// Group stage matches live under group_matches[<group name>]
for (const list of Object.values(data.group_matches ?? {})) {
  for (const m of list) rows.push([m.id, toIsoTimestamp(m.date, m.time)]);
}
// Knockouts in ko_matches.
for (const m of data.ko_matches ?? []) rows.push([m.id, toIsoTimestamp(m.date, m.time)]);

// Sort for stable diffs.
rows.sort(([a], [b]) => a.localeCompare(b));

// Emit a single upsert so re-runs apply schedule edits cleanly.
console.log('-- Generated by site/scripts/build-kickoffs-sql.mjs — do not edit by hand.');
console.log(`-- ${rows.length} matches found in public/data.json`);
console.log('INSERT INTO wc26_match_kickoffs (match_id, kickoff_at) VALUES');
const lines = rows.map(([id, ts], i) => {
  const tail = i === rows.length - 1 ? '' : ',';
  return `  ('${id}', '${ts}')${tail}`;
});
console.log(lines.join('\n'));
console.log('ON CONFLICT (match_id) DO UPDATE');
console.log('  SET kickoff_at = EXCLUDED.kickoff_at,');
console.log('      updated_at = NOW();');
